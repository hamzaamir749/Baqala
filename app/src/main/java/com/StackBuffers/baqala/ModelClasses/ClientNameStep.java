package com.StackBuffers.baqala.ModelClasses;

public class ClientNameStep {
    public int id;
    public String name;

    public ClientNameStep(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
